/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	unsigned char lpmMode = SLEEP_MODE;
	
	void (*lpmEntryFunPtr)(void) = NULL;
	void (*lpmExitFunPtr)(void) = NULL;
	
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

		char option = ' ';
		char strLpmMode[20] = "Sleep";
		
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, 0);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

		
		sprintf(uart_tx_buf, "\r\nSTM32 Low Power Demo \r\n\r\n1. Enter Sleep Mode \r\n2. Enter Stop Mode \r\n3. Enter Standby Mode\r\n");
		
		HAL_UART_Transmit(&huart2, (uint8_t *)uart_tx_buf, strlen(uart_tx_buf), 1000);
		
		HAL_UART_Receive(&huart2, (uint8_t *)uart_rx_buf, 1, 50000);
		
		option = uart_rx_buf[0];
		
		switch(option)
		{
			/* Sleep Mode */
			case '1':
				lpmMode = SLEEP_MODE;
				strcpy(strLpmMode, "Sleep");
				
				lpmEntryFunPtr = Lpm_Enter_SleepMode;
				lpmExitFunPtr = Lpm_Exit_SleepMode;
			break;
			
			/* Stop Mode */
			case '2':
				lpmMode = STOP_MODE;
				strcpy(strLpmMode, "Stop");
				
				lpmEntryFunPtr = Lpm_Enter_StopMode;
				lpmExitFunPtr = Lpm_Exit_StopMode;
			break;

			/* Standby Mode */
			case '3':
				lpmMode = STANDY_MODE;
				strcpy(strLpmMode, "Standby");
				
				lpmEntryFunPtr = Lpm_Enter_StandbyMode;
				lpmExitFunPtr = Lpm_Exit_StandbyMode;
			break;	

			default:
				sprintf(uart_tx_buf, "\r\nInvalid Option !");
			break;
			
		}
		
		/* Delay entry into Low Powre Mode */
		for(int i=0; i< SLEEP_DELAY; i++)
		{
			sprintf(uart_tx_buf, "\r\nEntering %s Mode in %d seconds .. ", strLpmMode, SLEEP_DELAY-i);
			HAL_UART_Transmit(&huart2, (uint8_t *)uart_tx_buf, strlen(uart_tx_buf), 1000);
			
			/* Wait for 1 second */
			HAL_Delay(1000);
		}
			
		/* Enter into target LPM mode */		
		(*lpmEntryFunPtr)();
		
		/* Exit from LPM mode*/
		(*lpmExitFunPtr)();
		
		sprintf(uart_tx_buf, "\r\n\r\nExit %s", strLpmMode);
		HAL_UART_Transmit(&huart2, (uint8_t *)uart_tx_buf, strlen(uart_tx_buf), 1000);
				
		
  }
  /* USER CODE END 3 */
}

